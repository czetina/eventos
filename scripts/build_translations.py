def main():
    po = polib.POFile()
    po.metadata = {
        "Project-Id-Version": "eventplanner",
        "Report-Msgid-Bugs-To": "",
        "POT-Creation-Date": datetime.datetime.now().strftime("%Y-%m-%d %H:%M%z"),
        "MIME-Version": "1.0",
        "Content-Type": "text/plain; charset=UTF-8",
        "Content-Transfer-Encoding": "8bit",
        "Language": "en",
    }
    for msgid, msgstr in TRANSLATIONS.items():
        po.append(polib.POEntry(msgid=msgid, msgstr=msgstr, occurrences=[("eventplanner", "")]))

    out_dir = BASE_DIR / "locale" / "en" / "LC_MESSAGES"
    out_dir.mkdir(parents=True, exist_ok=True)
    po.save(str(out_dir / "django.po"))
    po.save_as_mofile(str(out_dir / "django.mo"))
    print(f"Wrote {len(po)} translated strings to {out_dir}")


if __name__ == "__main__":
    main()
